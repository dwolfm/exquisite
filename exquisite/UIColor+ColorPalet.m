#import "UIColor+ColorPalet.h"

@implementation UIColor (ColorPalet)


+(UIColor *) tirqLight {
    return [UIColor colorWithRed: (CGFloat) 0 green: (CGFloat) 202/255 blue:(CGFloat) 187/255 alpha:1];
}

+(UIColor *) tirqMedium {
    return [UIColor colorWithRed: (CGFloat) 17/255 green:(CGFloat)  150/255 blue:(CGFloat)168/255 alpha:1];
}


@end
